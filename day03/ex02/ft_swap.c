#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int h;

	h = *a;
	*a = *b;
	*b = h;
}

int 	main(void)
{
	int n1;
	int n2;
	int *a;
	int *b;

	n1 = 5;
	n2 = 9;
	a = &n1;
	b = &n2;
	ft_swap(a, b);
	printf("n1 = %d\n", *a);
	printf("n2 = %d", *b);

	return (0);
}