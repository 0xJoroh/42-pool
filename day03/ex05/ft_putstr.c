#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}

int main(void)
{
	char *str;

	str = "You have 1 day to evaluate this project after closing your team";
	ft_putstr(str);
	return (0);
}