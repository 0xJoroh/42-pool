int	ft_str_is_alpha(char *str)
{
	int i;

	i = -1;
	if (str[0] == '\0')
		return (1);
	while (str[++i] != '\0')
	{
		if (str[i] > 122 || str[i] <= 57)
			return (0);
		if (90 < str[i] && str[i] < 97)
			return (0);
	}
	return (1);
}
